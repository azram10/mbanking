import UIKit

extension UIColor {
    
    static func appColor() -> UIColor {
        return UIColor(red:0.73, green:0.15, blue:0.44, alpha:1.0)
    }
    
    static func customGreen() -> UIColor {
        return UIColor(red:0.40, green:0.73, blue:0.49, alpha:1.0)
    }
    
    static func customRed() -> UIColor {
        return UIColor(red:0.73, green:0.15, blue:0.44, alpha:1.0)
    }
    
    static func keyboardText() -> UIColor {
        return UIColor(red:0.26, green:0.26, blue:0.26, alpha:1.0)
    }
    
    static func keyboardBackground() -> UIColor {
        return UIColor(red:0.85, green:0.87, blue:0.89, alpha:1.0)
    }
    
    static func keyboardHighlighted() -> UIColor {
        return UIColor(red:0.74, green:0.76, blue:0.80, alpha:1.0)
    }
    
}
